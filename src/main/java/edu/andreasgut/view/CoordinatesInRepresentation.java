package edu.andreasgut.view;

public class CoordinatesInRepresentation {
    int ring;
    int field;

    public CoordinatesInRepresentation(int ring, int field) {
        this.ring = ring;
        this.field = field;
    }

    public int getRing() {
        return ring;
    }

    public int getField() {
        return field;
    }
}
